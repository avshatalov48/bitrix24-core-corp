[B]So könnt Ihr Nachrichten senden[/B]

Im Intranet könnt Ihr alle möglichen Informationen, die für Arbeit nützlich und wichtig sind, anderen mitteilen. Die Nachricht kann an alle Mitarbeiter, oder eine bestimmte Gruppe oder aber an einen bestimmten Mitarbeiter gesendet werden. Die Empfänger werden dann Eure Nachricht im allgemeinen Activity Stream sehen.

[IMG WIDTH=500 HEIGHT=329]https://www.bitrix24.de/images/ustat/de/socnet2.png[/IMG]

Dabei könnt Ihr eine Datei oder ein Foto zur Nachricht anhängen, Links oder Tags hinzufügen oder einen bestimmten Kollegen mit Mention aufmerksam machen:

[IMG WIDTH=500 HEIGHT=330]https://www.bitrix24.de/images/ustat/de/socnet3.png[/IMG]

[B]So könnt Ihr einer Gruppe beitreten oder Eure eigene Gruppe erstellen[/B]

Gruppen sind ein sehr praktisches Tool, um an Projekten gemeinsam zu arbeiten. Und so könnt Ihr eine neue Gruppe erstellen, indem Ihr auf die Schaltfläche 'Erstellen' (oben links) klickt und dort den Punkt 'Gruppe' auswählt. Zu einer bereits existierenden Gruppe könnt Ihr auch beitreten, dafür müsst Ihr diese Gruppe im Intranet finden und eine Anfrage wegen Beitritts senden.

[B]So könnt Ihr Euren Kollegen danken[/B]

Die Verdienste anderer Kollegen sollten ausgezeichnet werden: Dafür habt Ihr die Schaltfläche 'Mehr' mit dem Punkt 'Auszeichnung'. Alles, was Ihr dann noch machen müsst, ist einen Kollegen auszuwählen, den Ihr auszeichnen wollt.

[IMG WIDTH=500 HEIGHT=338]https://www.bitrix24.de/images/ustat/de/socnet4.png[/IMG]

[B]Ihr könnt Eure Arbeit mit Bitrix24 selbst bewerten[/B]

Mit dem 'Unternehmenspuls', welcher als eine Art Monitoring-System funktioniert, könnt Ihr schnell feststellen, welche Intranet-Tools Ihr schon gut beherrscht und welche (noch) nicht.

[IMG WIDTH=500 HEIGHT=269]https://www.bitrix24.de/images/ustat/de/socnet5.png[/IMG]

Dabei könnt Ihr Eure persönlichen Ergebnisse mit denen von Kollegen vergleichen.

Die [B]Benachrichtigungen[/B] im Intranet halten Euch immer auf dem Laufenden. 

