[B]An Aufgaben gemeinsam arbeiten[/B]

Ihr könnt über Aufgaben zusammen mit Kollegen diskutieren und zur Aufgabe einen neuen [B]Kommentar  hinzufügen[/B] - das Gute dabei ist, dass Feedback viel schneller kommt als bei einer Diskussion per E-Mail. Alle Kommentare und angehängte Dateien werden direkt in den Aufgaben abgespeichert und bleiben somit immer verfügbar.

