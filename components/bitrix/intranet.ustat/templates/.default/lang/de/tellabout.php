[B]So könnt Ihr eine neue Aufgabe erstellen[/B]

Ihr klickt auf Erstellen > Aufgaben und schon habt Ihr das Formular, wo eine neue Aufgabe erstellt werden kann. Ihr könnt hier Verantwortliche definieren, Fristen der Erledigung festlegen und  das Wesen der Aufgabe detailliert beschreiben. Darüber hinaus könnt Ihr noch Mitwirkende für diese Aufgabe auswählen und bestimmte Dateien anhängen. Die Aufgabe kann einem Projekt zugeordnet werden, damit mehrere Kollegen daran arbeiten können.

