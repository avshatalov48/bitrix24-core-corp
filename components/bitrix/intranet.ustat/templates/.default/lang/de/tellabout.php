[B]Alle Fristen überwachen[/B]

Mit Zählern habt Ihr ein weiteres wichtiges Tool in den Aufgaben, mit dem Ihr alle Fristen überwachen könnt (die Zähler findet Ihr im Menü 'Mein Arbeitsplatz' sowie auf der Seite der Aufgaben).

[IMG WIDTH=500 HEIGHT=340]https://www.bitrix24.de/images/ustat/de/task4.png[/IMG]

Die Zähler zeigen, wie viele Aufgaben überfällig sind, wie viele nicht angezeigt wurden, und wie viele überhaupt keine Frist haben. Ihr sollt also versuchen,  die Zählerwerte so niedrig wie möglich zu bekommen. Ihr müsst vor allem realistische Fristen setzen, aber bei Bedarf können Fristen auch verschoben werden. Im Idealfall sollten die Zähler gar nicht erscheinen, denn das würde heißen, Ihr schafft alle Euren Aufgaben rechtzeitig.

[B]Zeitaufwand für die Aufgabe berücksichtigen[/B]

