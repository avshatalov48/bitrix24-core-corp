Ничего сложного здесь нет, вы даже сможете обойтись без привычного вам текстового редактора.  Чтобы поделиться файлом с коллегами и позволить всем участникам обсуждения вносить в текст правки, нужно только создать сообщение в «Живой Ленте» и прикрепить к нему нужный документ.

[IMG]https://www.bitrix24.ru/images/ustat/ru/disk1.png[/IMG]

Теперь все получатели сообщения не только увидят его в ленте, но и смогут просмотреть, скачать или даже отредактировать документ прямо в окне просмотра.

[IMG]https://www.bitrix24.ru/images/ustat/ru/disk2.png[/IMG]

[B]Как отредактировать документ в онлайне[/B]

Для редактирования документов вам не придется сохранять документ на свой компьютер – для этого используются облачные сервисы: Google Docs или MS Office Online. Вы можете выбрать один из них после нажатия на кнопку «Редактировать».

[IMG]https://www.bitrix24.ru/images/ustat/ru/disk3.png[/IMG]

Для того чтобы просматривать и редактировать документ вам нужно будет авторизоваться с вашей учетной записью Майкрософт или Google. Обратите внимание, в правом верхнем углу окна есть кнопка Вход. Если у вас нет ни одной учетной записи, вы ее легко создадите. Нажмите кнопку Вход и перейдите по ссылке «Зарегистрироваться». Это все бесплатно.

Теперь можно редактировать текст и сохранять изменения.

После сохранения вашей версии файла, в живой ленте появляется новый комментарий, к которому уже приложен документ с вашими исправлениями.

[IMG]https://www.bitrix24.ru/images/ustat/ru/disk4.png[/IMG]

[B]Где хранить документы и как обмениваться файлами[/B]

Для того чтобы ваши документы были всегда доступны лично вам или вашим коллегам, рекомендую использовать Битрикс24.Диск. Он позволит вам синхронизировать файлы, с которыми вы работаете, между вашим компьютером и хранилищем файлов на портале.

Зайдите в меню Инструменты > Файлы и установите на свой компьютер приложение Битрикс24, подключите Битрикс24.Диск.

[IMG]https://www.bitrix24.ru/images/ustat/ru/disk5.png[/IMG]

