[IMG]https://www.bitrix24.ru/images/ustat/ru/disk7.png[/IMG]

[B]Как разрешить или запретить просматривать ваши файлы[/B]

Всегда можно разграничить личные файлы (они будут храниться в личных папках), общедоступные (они попадают в общие файлы компании) и те, доступ к которым разрешен только определенным сотрудникам (в папке соответствующей группы).

Вы можете сами решать, кто сможет просматривать или редактировать ваш документ, например, когда вам нужно отправить презентацию и дать ссылку кому-то за пределы компании. При необходимости можно открыть доступ какому-то конкретному сотруднику на строго определённое время, да ещё и с доступом по паролю.

