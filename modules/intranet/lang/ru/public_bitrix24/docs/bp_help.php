  <h2>Последовательный процесс</h2>

  <p>Последовательную схему удобно применять для процессов имеющих определенный ограниченный жизненный цикл. (Например, создание текстового документа и его утверждение). Примерная схема такого процесса состоит из нескольких действий разного типа между начальной и конечной точкой процесса.</p>

  <p><img height=\"392\" width=\"227\" border=\"1\" alt=\"Пример простого последовательного процесса\" title=\"Пример простого последовательного процесса\" src=\"/images/bp/ru/2.png\" /></p>

  <br />

  <h2>Процесс со статусами</h2>

  <p>Процесс со статусами удобен для бизнес-процессов, не имеющих определенной временной схемы, которые могут повторяться и возвращаться из одного состояния в другое в силу производственной необходимости. (Например, постоянное обновление технической документации на выпускаемую продукцию.) Статусы - это не просто отметка об уровне готовности документа, они позволяют облегчить описание цикличности реального процесса средствами <b>Бизнес-процессов</b>. </p>

  <p>Создание такого шаблона сложнее, чем создание последовательного шаблона, зато представляет очень широкие возможности для автоматизации обработки информации. Примерная схема такого процесса состоит из нескольких статусов, включающих в себя определенные действия и условия смены статуса. </p>
  
  <img height=\"332\" width=\"757\" border=\"1\" title=\"Пример процесса со статусами\" src=\"/images/bp/ru/3.png\" />
 
  <p>Каждое действие внутри статуса, как правило, - собственный конечный последовательный процесс, который планируется в соответствии с работой над информацией в этом статусе.</p>

