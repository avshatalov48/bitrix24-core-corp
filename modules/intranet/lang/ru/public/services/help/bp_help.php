  <h2> Последовательный процесс</h2>

  <p>Последовательную схему удобно применять для процессов имеющих определенный ограниченный жизненный цикл. (Например, создание текстового документа и его утверждение). Примерная схема такого процесса состоит из нескольких действий разного типа между начальной и конечной точкой процесса.</p>

