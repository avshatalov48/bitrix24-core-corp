  <h1><a name='workflow'></a>Документооборот</h1>

  <p>Модуль <b>Документооборот</b> обслуживает линейные схемы работы с документами. Принцип его работы: перемещение документа по статусам в зависимости от выстроенной в схемы работы. </p>

  <p>Изначально в дистрибутиве корпоративного портала предусмотрено всего три статуса, достаточных для самой простой схемы документооборота. Для создания реальной схемы документооборота, действующей в конкретном проекте необходимо добавление новых статусов. Статусы может создавать только администратор портала, либо сотрудники, наделенные соответствующими правами доступа к управлению порталом.</p>
 <img height='148' width='724' border='1' title='Документы в разных статусах' alt='Документы в разных статусах' src='#SITE#images/ru/bp/1.png' />
  <p>Модуль <b>Документооборот</b> позволяет назначать сотрудников, ответственных за перевод документа в/из статуса в статус, и назначать сотрудников, имеющих права на редактирование документа в том или ином статусе. Также обеспечивается хранение копий документов в зависимости от настроек этого модуля. Настройку может производить только администратор портала.</p>

