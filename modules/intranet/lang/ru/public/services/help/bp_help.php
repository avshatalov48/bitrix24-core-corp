  <h1><a name='bizproc'></a>Бизнес-процессы</h1>

  <p>Модуль <b>Бизнес-процессы</b> - инструмент задания, выполнения и управления порядком обращения информации. Это намного более функциональный инструмент, чем модуль <b>Документооборот</b>. </p>

  <p><i><b>Бизнес-процесс</b> - это процесс обращения информации (документов) по заранее заданной схеме. В схеме бизнес-процесса могут быть заданы: </i></p>

  <ul>
    <li><i>	одна или несколько точек входа и выхода;</i></li>
   <i> </i>
    <li><i>последовательность действий (шагов, этапов, функций), совершаемых в заданном порядке и в определенных условиях.</i></li>
   </ul>
 <i></i>
  <p></p>

  <p>Процессы оборота информации в реальной предметной деятельности могут быть самыми различными. Публикация документа может потребовать самых разных порядков действий, задание разных условий выполнения, уведомлений сотрудников и так далее.</p>

  <p>Модуль <b>Бизнес-процессов</b> обладает универсальным механизмом создания и редактирования бизнес-процессов, доступный сотруднику - не программисту. Но надо понимать, что создание схемы бизнес-процесса требует определенного аналитического склада ума и детального знания реального процесса, действующего в компании.</p>

  <p>Такой универсальный механизм реализован за счет визуального программирования по технологии <b>drag&amp;drop</b> (бери и тащи), понятной и известной любому пользователю компьютера. Шаблон бизнес процессов создается в особом визуальном конструкторе. Модуль <b>Бизнес-процессов</b> позволяют сотруднику задать нужный порядок прохождения документа, отобразить специфичные потребности этого порядка с помощью простых графических схем.</p>

  <p>Порядок следования информации в компании задается с помощью шаблона бизнес-процесса, который конструируется из ряда действий. Действием может быть все, что угодно: от создания документа до отправки электронной почты или обновления строки в базе данных и так далее. </p>

  <p>В дистрибутив заложен ряд встроенных действий и типовых бизнес-процесов, которые могут быть использованы для выполнения работ общего назначения. Таких действий - несколько десятков, типовых процессов - несколько экземпляров. </p>

  <p>При помощи модуля <b>Бизнес-процессы</b> могут быть описаны два способа обращения документов: </p>

  <ul>
    <li><b>последовательный бизнес-процесс</b> - действия выполняются одно за другим от точки входа до точки выхода; </li>

    <li><b>бизнес-процесс со статусами</b> - начала и конца не имеет, в процессе работы происходит переход из одного состояния в другое, может быть завершен в любой точке.</li>
   </ul>

