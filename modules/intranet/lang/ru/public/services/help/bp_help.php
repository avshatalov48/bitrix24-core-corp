  <br />

  <h2>Процесс со статусами</h2>

  <p>Процесс со статусами удобен для бизнес-процессов, не имеющих определенной временной схемы, которые могут повторяться и возвращаться из одного состояния в другое в силу производственной необходимости. (Например, постоянное обновление технической документации на выпускаемую продукцию.) Статусы - это не просто отметка об уровне готовности документа, они позволяют облегчить описание цикличности реального процесса средствами модуля <b>Бизнес-процессы</b>. </p>

  <p>Создание такого шаблона сложнее, чем создание последовательного шаблона, зато представляет очень широкие возможности для автоматизации обработки информации. Примерная схема такого процесса состоит из нескольких статусов, включающих в себя определенные действия и условия смены статуса. </p>
 <img height='332' width='757' border='1' title='Пример процесса со статусами' src='#SITE#images/ru/bp/3.png' />
  <p>Каждое действие внутри статуса, как правило, - собственный конечный последовательный процесс, который планируется в соответствии с работой над информацией в этом статусе.</p>

