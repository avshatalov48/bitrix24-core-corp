<h2>В чем прелесть двусторонней синхронизации?</h2>

<p>Такая синхронизация позволяет держать в актуальном состоянии данные на Портале и MS Outlook одновременно, например, личные контакты. Попробуйте сделать это прямо сейчас самостоятельно и вы прочувствуйте, насколько удобнее стало работать. Чем именно? Допустим, находясь на борту самолета, вы продумали и запланировали множество всяческих встреч, задач и мероприятий для компании. Теперь воспользуйтесь этой самой возможностью <b>двусторонней синхронизации календарей с MS Outlook</b> - и ваши наработки отразятся в календаре на Портале! При этом <b>никакого ручного дублирования информации</b>, а способы работы - привычные и старые. Быстро, удобно и очень эффективно!
