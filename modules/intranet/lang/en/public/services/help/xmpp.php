<h2>Private <b>XMPP Server</b></h2>
 The private <b>XMPP server</b> was created to solve the most essential problem any business may encounter: the creation of
a <b>secure messaging system</b>. The private server allows avoiding the use of unsafe ICQ or other public messaging
services while keeping the tool that you are used to! The Portal server runs over the <b>Jabber</b> protocol
which is supported by the most of the popular messaging applications. Effectively, it means that you can communicate
with your colleagues using your favorite applications: <b>Miranda, Pidgin, Psi</b> and others.
